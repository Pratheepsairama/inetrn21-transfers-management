package com.tcs.inetrn21.ms.transfersmanagement.transfersmanagement;

public class TransferNotFoundExceptionClass extends RuntimeException {
public TransferNotFoundExceptionClass() {
		
	}

	public TransferNotFoundExceptionClass(String message) {
		super(message);
		
	}

	public TransferNotFoundExceptionClass(Throwable cause) {
		super(cause);
		
	}

	public TransferNotFoundExceptionClass(String message, Throwable cause) {
		super(message, cause);
		
	}

	public TransferNotFoundExceptionClass(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}


}
